use alloc::vec::Vec;

/// Single DDI definition entry.
#[derive(Debug, Clone, Copy, PartialEq)]
pub struct DDIDefinition {
    pub ddi: u16,
    pub name: &'static str,
    pub unit: &'static str,
    pub resolution: f64,
    pub min_value: i32,
    pub max_value: i32,
}

/// Legacy alias.
pub type DDIEntry = DDIDefinition;

/// AgIsoStack-compatible DDI lookup entry facade.
///
/// This keeps the compact generated Rust table unchanged while exposing the
/// same shape as AgIsoStack++ `DataDictionary::Entry`: unit symbol,
/// human-readable unit description, resolution, and display range. Unknown
/// DDIs return the same sentinel entry used by AgIsoStack++ instead of `None`.
#[derive(Debug, Clone, Copy, PartialEq)]
pub struct DataDictionaryEntry {
    pub ddi: u16,
    pub name: &'static str,
    pub unit_symbol: &'static str,
    pub unit_description: &'static str,
    pub resolution: f64,
    pub display_range: (f64, f64),
}

impl DataDictionaryEntry {
    #[must_use]
    pub const fn unknown() -> Self {
        Self {
            ddi: u16::MAX,
            name: "Unknown",
            unit_symbol: "Unknown",
            unit_description: "Unknown",
            resolution: 0.0,
            display_range: (0.0, 0.0),
        }
    }
}

